#Sorting a list permanetly with sort() method.
cars = ['bmw', 'audi', 'toyota', 'bentley']
cars.sort()
print(cars)

cars.sort(reverse=True)
print(cars)

#Sorting a list temporarily with the sorteed() function
cars = ['bmw', 'audi', 'toyota', 'bentley']
print('Here is the original list: \n',cars)

print('Here is the sorted list: \n', sorted(cars))
print('Here is the original list again: \n',cars)

